    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        ProductDAO pd = new ProductDAO();
        String pname = request.getParameter("pname");
        String name = request.getParameter("name");
        String img = request.getParameter("img");
        double price = Double.parseDouble(request.getParameter("price"));
        String title = request.getParameter("title");
        String des = request.getParameter("des");
        int cateId = Integer.parseInt(request.getParameter("cate"));
        int quantity = Integer.parseInt(request.getParameter("quantity"));
        Product p = new Product(name, img, price, title, des, cateId, quantity);
        pd.UpdateProduct(p, pname);
        List<Product> list1 = pd.getAllProduct1();  
        request.getSession().setAttribute("listP1", list1);
        request.getRequestDispatcher("deleteproduct.jsp").forward(request, response);
    }

